export function getInputId(id: string) {
  return `text-field:${id}`;
}

export function getLabelId(id: string) {
  return `text-field:${id}:label`;
}

export function getDescriptionId(id: string) {
  return `text-field:${id}:description`;
}

export function getMessageId(id: string) {
  return `text-field:${id}:message`;
}
