export function getTextareaId(id: string) {
  return `textarea:${id}`;
}

export function getLabelId(id: string) {
  return `textarea:${id}:label`;
}

export function getDescriptionId(id: string) {
  return `textarea:${id}:description`;
}

export function getMessageId(id: string) {
  return `textarea:${id}:message`;
} 